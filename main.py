lang = input("What language do you want to use? [EN,JP]: ")
# # Accounts for spacing between words
input_sentence = input("Enter the sentence you want morse-coded: ").lower().replace(" ", "/")


def lang_to_morse():
    morse_list = []
    for letter in input_sentence:
        try:
            if lang == "EN" or lang == "en":
                morse_list.append(latin_morse_code_chart[letter])
            elif lang == "JP" or lang == "jp":
                morse_list.append(wabun_code_chart[letter])
                morse_list.append(wabun_code_chart["/"])
            else:
                print("Incorrect Lang: Try again")
                return
        except KeyError:
            print("Error: Enter alphanumeric characters.")
            return

    morse_string = ""
    morse_string = morse_string.join(morse_list)
    print(morse_string)


lang_to_morse()

